//	3. Найдите значение функции: z = 2 * x + ( y – 2 ) * 5.
public class Task03 {

	public static void main(String[] args) {
		double x;
		double y;

		double z;

		x = 4.0;
		y = 6.0;

		z = 2 * x + (y - 2) * 5;

		System.out.println("Для x = " + x + " и y = " + y + " значение функции 2 * x + ( y - 2 ) * 5 = " + z);
	}

}
