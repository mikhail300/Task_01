// 5. Составить алгоритм нахождения среднего арифметического двух чисел.
public class Task05 {

	public static void main(String[] args) {
		double a;
		double b;

		a = 4.0;
		b = 8.0;

		double avg;

		avg = (a + b) / 2;

		System.out.println("Среднее арифметическое для a = " + a + ", b = " + b + " будет равно " + avg);
	}

}
